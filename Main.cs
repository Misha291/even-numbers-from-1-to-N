using System;  // Подключаем стандартную библиотеку C# для работы с консолью и другими базовыми функциями

namespace MyProj  // Объявляем пространство имен - это как "папка" для нашего кода
{
    class Program  // Создаем главный класс программы
    {
        static void Main()  // Главный метод, который выполняется при запуске программы
        {
            // ЗАПРОС ВВОДА ОТ ПОЛЬЗОВАТЕЛЯ
            Console.Write("Введите число:");  // Выводим подсказку без перехода на новую строку

            // ЧТЕНИЕ И ПРЕОБРАЗОВАНИЕ ВВОДА
            var n = int.Parse(Console.ReadLine());  // Читаем ввод пользователя и преобразуем строку в число
            // Console.ReadLine() - читает то, что ввел пользователь
            // int.Parse() - преобразует текст в число
            // var n - автоматически определяет тип переменной (здесь будет int)

            // ПУСТАЯ СТРОКА ДЛЯ КРАСОТЫ
            Console.WriteLine();  // Просто переходим на новую строку, чтобы разделить ввод и вывод

            // ВЫВОД ЗАГОЛОВКА
            Console.WriteLine($"Четные числа от 1 до {n}:");  // Выводим заголовок с подставленным числом n
            // $ перед строкой позволяет использовать {n} для подстановки значения переменной

            // ПЕРЕБОР ЧИСЕЛ ОТ 1 ДО N
            for (int i = 1; i <= n; i++)  // Цикл: i от 1 до n включительно
            // int i = 1 - начальное значение счетчика
            // i <= n - условие продолжения цикла (пока i меньше или равно n)
            // i++ - увеличение i на 1 после каждой итерации
            {
                // ПРОВЕРКА НА ЧЕТНОСТЬ
                if (i % 2 == 0)  // Если остаток от деления i на 2 равен 0
                // % - оператор взятия остатка от деления
                // если число делится на 2 без остатка, значит оно четное
                {
                    // ВЫВОД ЧЕТНОГО ЧИСЛА
                    Console.WriteLine(i);  // Выводим четное число и переходим на новую строку
                }
            }
            // КОНЕЦ ПРОГРАММЫ
        }
    }
}
